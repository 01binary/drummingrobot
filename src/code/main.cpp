#include <ros/ros.h>
#include "robot.h"

/*----------------------------------------------------------*\
| Namespace
\*----------------------------------------------------------*/

using namespace str1ker;

/*----------------------------------------------------------*\
| Module
\*----------------------------------------------------------*/

int main(int argc, char** argv)
{
    ros::init(argc, argv, "robot");
    ros::NodeHandle node;

    bool init = robot()
        .logo()
        .deserialize()
        .init();

    if (!init) return 1;

    ros::Rate rate(1000);

    while(node.ok())
    {
        rate.sleep();
    }

    return 0;
}